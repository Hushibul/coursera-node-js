let rect = require("./rectangle");

function solveReactangel(l, b) {
  console.log("Solving for rectangle with l = " + l + " and b = " + b);

  rect(l, b, (err, rectangle) => {
    if (err) {
      console.log("Error: " + err.message);
    } else {
      console.log(
        "The area of the rectangle of dimensions l = " +
          l +
          " and b = " +
          b +
          " is " +
          rectangle.area()
      );
      console.log(
        "The perimeter of the rectangle of dimensions l = " +
          l +
          " and b = " +
          b +
          " is " +
          rectangle.perimeter()
      );
    }
  });
}

solveReactangel(2, 5);
solveReactangel(3, 4);
solveReactangel(0, 5);
solveReactangel(-5, 5);
